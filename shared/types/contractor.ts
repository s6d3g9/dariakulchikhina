import { z } from 'zod'

export const ContractorSchema = z.object({
  id: z.number(),
  slug: z.string().min(1).max(200),
  name: z.string().min(1).max(200),
  companyName: z.string().max(200).optional().nullable(),
  contactPerson: z.string().max(200).optional().nullable(),
  phone: z.string().max(50).optional().nullable(),
  email: z.string().max(200).optional().nullable(),
  inn: z.string().max(20).optional().nullable(),
  kpp: z.string().max(20).optional().nullable(),
  ogrn: z.string().max(20).optional().nullable(),
  bankName: z.string().max(200).optional().nullable(),
  bik: z.string().max(20).optional().nullable(),
  settlementAccount: z.string().max(30).optional().nullable(),
  correspondentAccount: z.string().max(30).optional().nullable(),
  legalAddress: z.string().max(500).optional().nullable(),
  factAddress: z.string().max(500).optional().nullable(),
  workTypes: z.array(z.string().max(200)).max(50),
  roleTypes: z.array(z.string().max(200)).max(50).optional().default([]),
  contractorType: z.enum(['master', 'company']).default('master'),
  parentId: z.number().optional().nullable(),
  notes: z.string().max(5000).optional().nullable(),
  messenger: z.string().max(100).optional().nullable(),
  messengerNick: z.string().max(100).optional().nullable(),
  website: z.string().max(500).optional().nullable(),
  // Паспортные данные
  passportSeries: z.string().max(10).optional().nullable(),
  passportNumber: z.string().max(10).optional().nullable(),
  passportIssuedBy: z.string().max(500).optional().nullable(),
  passportIssueDate: z.string().max(50).optional().nullable(),
  passportDepartmentCode: z.string().max(20).optional().nullable(),
  birthDate: z.string().max(50).optional().nullable(),
  birthPlace: z.string().max(500).optional().nullable(),
  registrationAddress: z.string().max(500).optional().nullable(),
  snils: z.string().max(20).optional().nullable(),
  // Доп. контакты
  telegram: z.string().max(100).optional().nullable(),
  whatsapp: z.string().max(100).optional().nullable(),
  city: z.string().max(200).optional().nullable(),
  workRadius: z.string().max(100).optional().nullable(),
  // Финансовые / организационные
  taxSystem: z.string().max(100).optional().nullable(),
  paymentMethods: z.array(z.string().max(100)).max(20).optional().default([]),
  hourlyRate: z.string().max(50).optional().nullable(),
  hasInsurance: z.boolean().optional().default(false),
  insuranceDetails: z.string().max(2000).optional().nullable(),
  education: z.string().max(1000).optional().nullable(),
  certifications: z.array(z.string().max(500)).max(50).optional().default([]),
  experienceYears: z.number().optional().nullable(),
})
export const CreateContractorSchema = z.object({
  slug: z.string().min(1).max(200).regex(/^[a-z0-9_-]+$/),
  name: z.string().min(1).max(200),
  contractorType: z.enum(['master', 'company']).default('master'),
  parentId: z.number().optional().nullable(),
})
export const UpdateContractorSchema = ContractorSchema.omit({ id: true, slug: true }).partial()
export type Contractor = z.infer<typeof ContractorSchema>
