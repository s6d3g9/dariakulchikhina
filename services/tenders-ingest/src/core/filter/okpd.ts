/**
 * Returns true if at least one of the tender's OKPD2 codes matches the
 * whitelist by prefix. Empty input → false (no codes ⇒ not in scope).
 */
export function matchesOkpd2Whitelist(
  okpd2Codes: readonly string[],
  whitelist: readonly string[] = CITYOFROADS_OKPD2_WHITELIST,
): boolean {
  if (okpd2Codes.length === 0) return false
  for (const code of okpd2Codes) {
    if (!code) continue
    for (const allowed of whitelist) {
      if (code === allowed || code.startsWith(allowed + '.')) {
        return true
      }
    }
  }
  return false
}
